package org.virtue.model.entity.old_combat.actionbar;

/**
 * Represents the ability types.
 * 
 * @author Emperor
 * 
 */
public enum AbilityType {

	/**
	 * The basic, adrenaline boosting ability type.
	 */
	BASIC,

	/**
	 * Represents a treshold ability, draining 50% adrenaline.
	 */
	TRESHOLD,

	/**
	 * Represents an ultimate ability, draining full adrenaline.
	 */
	ULTIMATE;
}